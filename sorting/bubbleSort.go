func BubbleSort(arr []int) { // arr := []int{5, 3, 8, 4, 2}
	n := len(arr)
	for i := 0; i < n-1; i++ {
		swapped := false
		for j := 0; j < n-i-1; j++ {
			if arr[j] > arr[j+1] {
				arr[j], arr[j+1] = arr[j+1], arr[j]
				swapped = true
			}

		}
		if swapped == false {
			break
		}
	}

}
